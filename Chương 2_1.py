n = input()
if n == 'A+' or n == 'A':
    print(4.0)
elif n == 'A-':
    print(3.7)
elif n == 'B+':
    print(3.3)
elif n == 'B':
    print(3.0)
elif n == 'B-':
    print(2.7)
elif n == 'C+':
    print(2.3)
elif n == 'C':
    print(2.0)
elif n == 'C-':
    print(1.7)
elif n == 'D+':
    print(1.3)
elif n == 'D':
    print(1.0)
else:
    print(0)